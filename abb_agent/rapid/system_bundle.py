def _write_latin1_crlf(path: Path, text: str) -> None:
    """ABB 控制器友好的写法：ISO-8859-1 编码 + CRLF 行终止符。

    非 Latin-1 字符替换为 '?'（写入前不该有，但作为兜底；调用方应保证 ASCII）。
    """
    crlf = text.replace("\r\n", "\n").replace("\n", "\r\n")
    path.write_bytes(crlf.encode("iso-8859-1", errors="replace"))


def bundle_for_controller(
    out_dir: Path,
    module_code: str,
    *,
    controller: ControllerKind = "IRC5",
    force: bool = False,
) -> dict[str, Path]:
    """把单个模块代码扩展成完整可加载目录。

    - .pgf 与 .sys 用 ISO-8859-1 + CRLF 写入（ABB 控制器要求）
    - .mod 用 UTF-8 写入（保留 LLM 可能输出的中文注释，新一代 RobotWare 支持）
    - README.md 用 UTF-8（开发者读，不上控制器）

    Args:
        out_dir: 输出目录
        module_code: 主模块 RAPID 代码
        controller: 目标控制器类型
        force: True 时覆盖已存在的 BASE.sys / T_ROB1.pgf；默认 False 拒绝覆盖

    Returns:
        dict 键是文件角色（module/pgf/sys/readme），值是落盘 Path。
        多出一个 'manifest' 键指向 MANIFEST.ok（写完成功标志）。

    Raises:
        FileExistsError: out_dir 已存在 BASE.sys/T_ROB1.pgf 且 force=False
    """
    out_dir.mkdir(parents=True, exist_ok=True)

    module_name = extract_module_name(module_code)
    module_filename = f"{module_name}.mod"

    module_path = out_dir / module_filename
    sys_path = out_dir / "BASE.sys"
    pgf_path = out_dir / "T_ROB1.pgf"
    readme_path = out_dir / "README.md"
    manifest_path = out_dir / "MANIFEST.ok"

    # 防覆盖检查
    if not force:
        existing = [p for p in (sys_path, pgf_path, module_path) if p.exists()]
        if existing:
            names = ", ".join(p.name for p in existing)
            raise FileExistsError(
                f"目标目录 {out_dir} 已存在 {names}；用 force=True 覆盖。"
            )

    # 删除旧 MANIFEST.ok（写完后再生成 → 半写状态可见）
    if manifest_path.exists():
        manifest_path.unlink()

    module_path.write_text(module_code, encoding="utf-8")
    _write_latin1_crlf(sys_path, build_sys_module(controller=controller))
    _write_latin1_crlf(pgf_path, build_pgf(["BASE", module_name]))
    readme_path.write_text(
        build_load_readme(module_filename=module_filename, controller=controller),
        encoding="utf-8",
    )

    # 全部写完才创建标志，让外部能区分「完整」vs「半写」
    manifest_path.write_text(
        f"bundle complete\ncontroller={controller}\nmodule={module_filename}\n",
        encoding="utf-8",
    )

    return {
        "module": module_path,
        "sys": sys_path,
        "pgf": pgf_path,
        "readme": readme_path,
        "manifest": manifest_path,
    }
