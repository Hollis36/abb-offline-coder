"""Pack&Go 兼容的系统级文件包生成器。

仅生成单个 .mod 不足以让 IRC5P 控制器直接加载并运行。完整可加载包至少需要：
  - <ModuleName>.mod  : 主程序模块（已有）
  - BASE.sys          : 系统模块，含 Home 位 / 错误恢复 trap
  - <Task>.pgf        : 程序文件列表，控制器据此加载模块到 T_ROB1
  - README.md         : 三种加载方式的说明（RobotStudio / FlexPendant / USB）

本模块输出这四件，让用户能直接把目录拷到控制器 HOME 文件夹或通过 RobotStudio
Pack&Go 还原。
"""
from __future__ import annotations

from pathlib import Path
from typing import Literal

from abb_agent.rapid.module_template import extract_module_name

ControllerKind = Literal["IRC5", "IRC5P"]


def _extension_for(name: str) -> str:
    """系统模块用 .sys，其余用 .mod。"""
    return ".sys" if name.upper() == "BASE" else ".mod"


def build_pgf(module_names: list[str]) -> str:
    """生成 .pgf XML 文件，列出 T_ROB1 任务要加载的所有模块。

    ABB 控制器读取 .pgf 时按顺序加载文件。系统模块（.sys）通常排在最前。
    encoding 声明 ISO-8859-1；写盘时一致使用 latin-1 + CRLF。
    """
    lines = [
        '<?xml version="1.0" encoding="ISO-8859-1" ?>',
        "<Program>",
    ]
    for name in module_names:
        ext = _extension_for(name)
        lines.append(f"    <Module>{name}{ext}</Module>")
    lines.append("</Program>")
    return "\n".join(lines) + "\n"


def build_sys_module(*, controller: ControllerKind = "IRC5") -> str:
    """生成 BASE.sys 系统模块。

    包含：
      - 标准 Home 位 robtarget（pHome）
      - 全局错误处理 trap (errhandler)
      - 复位例程 PROC reset_to_home() 用于异常后回安全位

    SYSMODULE 属性确保模块在 PP-to-main 时仍保留。

    注意：本模块输出**全 ASCII**，因为 RobotWare 老版本加载 .sys 时默认按
    ISO-8859-1 解码，非 ASCII 字符（中文注释）会导致加载失败。生成的中文
    解释请放到与 .sys 同目录的 README.md 而不是 .sys 内部。
    """
    body_recovery_comment = (
        "        ! IRC5P recovery: brushdata handles gun timing automatically;\n"
        "        ! reset auxiliary fan/atom signals here, then call reset_to_home\n"
        if controller == "IRC5P"
        else "        ! Recovery: shut spray, then call reset_to_home\n"
    )

    extra_io_resets = (
        "        Reset doFanOn;\n"
        "        Reset doAtomOn;\n"
        if controller == "IRC5P"
        else "        Reset doSprayOn;\n"
    )

    return (
        "MODULE BASE (SYSMODULE)\n"
        f"    ! Generated by abb-agent -- controller={controller}\n"
        "    ! NOTE: this file must remain pure ASCII to load on RobotWare\n"
        "    !       that defaults to ISO-8859-1 decoding.\n"
        "\n"
        "    ! ---------------- Home position ----------------\n"
        "    ! Safe Home pose. Field engineer MUST re-teach this on-site;\n"
        "    ! below is placeholder only.\n"
        "    PERS robtarget pHome := "
        "[[0,0,500],[1,0,0,0],[0,0,0,0],[9E9,9E9,9E9,9E9,9E9,9E9]];\n"
        "\n"
        "    ! ---------------- Error trap ----------------\n"
        "    VAR intnum iErrHandler;\n"
        "\n"
        "    TRAP errhandler\n"
        f"{body_recovery_comment}"
        f"{extra_io_resets}"
        "        ! Do NOT MoveJ/MoveL inside the trap; defer motion to reset_to_home.\n"
        "    ENDTRAP\n"
        "\n"
        "    ! ---------------- Recovery routine ----------------\n"
        "    PROC reset_to_home()\n"
        "        ! Call after exception handling to return robot to Home.\n"
        "        ConfL\\Off;\n"
        "        SingArea\\Wrist;\n"
        "        MoveJ pHome, v500, fine, tool0;\n"
        "    ENDPROC\n"
        "\n"
        "    ! ---------------- System init ----------------\n"
        "    PROC sys_init()\n"
        "        ! Called on power-on / PP-to-main.\n"
        "        ! TODO: bind error trap to a real EIO signal that exists in\n"
        "        ! your controller's EIO.cfg, e.g.:\n"
        "        !     CONNECT iErrHandler WITH errhandler;\n"
        "        !     ISignalDI \\Single, diEmergencyStop, 1, iErrHandler;\n"
        "        ! Left commented out so this module compiles on a fresh\n"
        "        ! controller without diEmergencyStop defined.\n"
        "    ENDPROC\n"
        "\n"
        "ENDMODULE\n"
    )


